                    <h4>A COMISSÃO</h4>
                    <p>A Comissão Disciplinar Desportiva da Liga Municipal de Futebol de Taubaté é o órgão judicante do futebol na cidade de Taubaté e é
                      composta pelos auditores e Procuradoria da Justiça Desportiva.</p>

                    <p>A Comissão Disciplinar atualmente é Presidida pelo <b>Gontran de Paiva Nasser Netor</b>, e é constituída por 04 auditores, aos incumbe o
                      processamento e julgamento dos procedimentos disciplinares no âmbito da Justiça Desportiva. A CDD também analisa outros procedimentos, conforme previstos no Código Brasileiro de Justiça Desportiva.</p>
                    <p>O Procurador geral da CDD é o Dr. Rodrigo Rosa de Oliveira, a quem incumbe, dentre outras atribuições, o oferecimento de denúncias
                      por infrações disciplinares.</p>

                    <p>A CDD e a Procuradoria atuam nos julgamentos das infrações disciplinares ocorridas nos campeonatos organizados pela Liga Municipal
                      de Futebol de Taubaté.</p>

                    <p>A CDD julga os processos originados dos relatórios semanais e do dia imediato à realização das partidas, elaboradas pelos árbitros
                      e enviadas à Procuradoria do CDD. De acordo com as disposições do CBJD e do entendimento do Procurador Dr. Rodrigo, são
                      apresentados as respectivas denúncias.</p>

                    <p>Após as denúncias, o secretário do CDD faz a necessária citação dos denunciados e encaminhados por e-mail aos infratores e
                      divulgados no site da LMFT (<a href="www.ligataubate.com.br">www.ligataubate.com.br</a>) indicando os artigos que foram infringidos pelos denunciados, bem como a data
                      e horário do julgamento dos processos.</p>

                    <p>No julgamento dos processos disciplinares, após a manifestação do Procurado e da defesa dos denunciados, os mesmos são levados
                      a julgamento perante a CDD.</p>

                    <p>Após o julgamento, o secretário da CDD transcreve em ata o resultado dos julgamentos, publicando-os no dia imediato também no
                      link da CDD no site da LMFT.</p>
