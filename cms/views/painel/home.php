<h1>Sistema de Gestão LMFT</h1>

<p> Olá bem vindo, nesta 1ª etapa finalizaremos o cadastro do seu clube e logo em seguida começaremos o cadastro dos atletas</p>

<ol>
	<li> Finalizar cadastro do clube </li>
	<blockquote>Acesse à esquerda o meu <b> CLUBES</b>, logo em seguida clique em <b> MEU CLUBE</b>, preencha todos os campos corretamente e clique em Atualizar, pronto! Cadastro do clube finalizado</blockquote>	

	<li> Cadastro de Atleta </li>
	<blockquote>Acesse à esquerda <b> ATLETAS</b>, logo em seguida clique em <b> ADICIONAR ATLETA</b>, preencha todos os campos corretamente e clique em Cadastrar, pronto! Cadastro de Atleta finalizado

		<br /> <b> Importante: </b> Após realizar o cadastro do atleta e perceber que a imagem ficou com a rotação errada, utilize o link à direita da imagem (GIRAR IMAGEM) para corrigir a orientação

		

	</blockquote>	

	<li> Imprimir ficha de inscrição do atleta</li>
	<blockquote>

		Acesse o cadastro do atleta em questão e localize à direita da tela o link <b> Imprmir inscrição</b>, uma nova janela será aberta disponibilizando a impressão. Após realizar a impressão poderá fechar a janela e continuar o processo para os demais.

	</blockquote>




		<li> Atualizar e-mail de contato (<font color='red'> NOVO</font>) </li>
	<blockquote>

		Acesse sua área administrativa, localize as informações do seu clube. Agora você poderá atualizar o e-mail de contato - este também será utilizado para redefinição de senha.

	</blockquote>



			<li> Esqueci minha senha (<font color='red'> NOVO</font>) </li>
	<blockquote>

		Na tela de login caso você esqueça sua senha, poderá redefinir informando o e-mail de contato, mas não esqueça de atualizá-lo conforme item 4.

	</blockquote>



			<li> Alterar minha senha (<font color='red'> NOVO</font>) </li>
	<blockquote>

		Dentro da edição do clube, localize o campo senha, informe a senha desejada e clique em atualizar. Pronto, senha alterada com sucesso!

	</blockquote>




</ol>


